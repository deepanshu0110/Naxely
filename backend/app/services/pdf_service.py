    company_name = user_data.get('company_name', '')

    output_dir = OUTPUT_ROOT / str(report_id)
    output_dir.mkdir(parents=True, exist_ok=True)
    pdf_path = str(output_dir / 'report.pdf')

    def on_page(canvas, doc):
        _on_page(canvas, doc, add_watermark=is_free, is_white_label=is_white_label, company_name=company_name)

    doc = SimpleDocTemplate(
        pdf_path,
        pagesize=A4,
        leftMargin=MARGIN,
        rightMargin=MARGIN,
        topMargin=MARGIN,
        bottomMargin=MARGIN,
    )

    body_style = ParagraphStyle(
        'BodyText2',
        fontName='IBMPlexSans',
        fontSize=10,
        textColor=HexColor('#374151'),
        alignment=TA_JUSTIFY,
        spaceAfter=8,
        leading=14,
    )

    toc_h1_style = ParagraphStyle(
        'TOCH1',
        fontName='IBMPlexSans',
        fontSize=11,
        textColor=HexColor('#374151'),
        leftIndent=20,
        spaceBefore=4,
        spaceAfter=2,
        leading=14,
    )

    story = []
    body_story: list = []
    toc_entries = [('Cover Page', '1'), ('Table of Contents', '2')]
    toc_page = 3

    # ────────────────────────────────────────────────────────────
    # SECTION 1 — Cover Page (into main story)
    # ────────────────────────────────────────────────────────────
    logo_path = None
    logo_signed_url = user_data.get('logo_url')
    if logo_signed_url:
        logo_path = _download_logo(logo_signed_url)

    title = config.get('title', 'Marketing Performance Report')
    company_name = user_data.get('company_name', '')

    story.append(_CoverBand(
        title=title, company_name=company_name,
        logo_path=logo_path, brand_color_hex=brand_color,
    ))
    story.append(_CoverMotif(brand_color))

    kpis = _compute_kpi_data(df, config, ai_content, brand_color)
    hero = max(kpis, key=lambda k: abs(k.get('trend_pct', 0))) if kpis else None

    if hero:
        trend_pct = hero['trend_pct']
        if trend_pct > 0:
            arrow_color_hex = MINT
        elif trend_pct < 0:
            arrow_color_hex = RED
        else:
            arrow_color_hex = '#6B7280'
        sign = '+' if trend_pct > 0 else ''
        class _CoverHeroStat(Flowable):
            def __init__(self, hero, sign, trend_pct, arrow_color_hex, brand_color):
                Flowable.__init__(self)
                self.hero = hero
                self.sign = sign
                self.trend_pct = trend_pct
                self.arrow_color = HexColor(arrow_color_hex)
                self.brand_color = HexColor(brand_color)
                self._width = content_width
                self.height = 56

            def wrap(self, availWidth, availHeight):
                return (self._width, self.height)

            def draw(self):
                cx = self._width / 2
                label = f'{self.hero["name"]}: {self.sign}{self.trend_pct:.1f}%'
                self.canv.setFillColor(self.brand_color)
                self.canv.setFont('Fraunces-SemiBold', 22)
                self.canv.drawCentredString(cx, 18, label)
                s = 9
                arrow_x = cx + self.canv.stringWidth(label, 'Fraunces-SemiBold', 22) / 2 + 16
                arrow_y = 28
                if self.trend_pct > 0:
                    p = self.canv.beginPath()
                    p.moveTo(arrow_x - s, arrow_y - s)
                    p.lineTo(arrow_x + s, arrow_y - s)
                    p.lineTo(arrow_x, arrow_y + s)
                    p.close()
                elif self.trend_pct < 0:
                    p = self.canv.beginPath()
                    p.moveTo(arrow_x - s, arrow_y + s)
                    p.lineTo(arrow_x + s, arrow_y + s)
                    p.lineTo(arrow_x, arrow_y - s)
                    p.close()
                else:
                    p = self.canv.beginPath()
                    p.moveTo(arrow_x - s, arrow_y - s)
                    p.lineTo(arrow_x - s, arrow_y + s)
                    p.lineTo(arrow_x + s, arrow_y)
                    p.close()
                self.canv.setFillColor(self.arrow_color)
                self.canv.drawPath(p, fill=1, stroke=0)
        story.append(_CoverHeroStat(hero, sign, trend_pct, arrow_color_hex, brand_color))

    story.append(_CoverRule(GRID_LINE))

    info_style = ParagraphStyle(
        'CoverInfo', fontName='IBMPlexSans',
        fontSize=10, textColor=HexColor('#6B7280'),
        alignment=TA_CENTER, spaceAfter=4,
    )
    date_from = config.get('date_from', 'N/A')
    date_to = config.get('date_to', 'N/A')
    if date_from and date_to and date_from != 'N/A':
        story.append(Paragraph(f'From {date_from} to {date_to}', info_style))
    prepared_by = (config.get('brand') or {}).get('prepared_by', '')
    if prepared_by:
        story.append(Paragraph(f'Prepared by: {prepared_by}', info_style))
    story.append(Paragraph(
        f'Prepared on: {datetime.now().strftime("%B %d, %Y")}', info_style,
    ))
    story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 2 — Executive Summary
    # ────────────────────────────────────────────────────────────
    if 'executive_summary' in config.get('sections', []) and ai_content.get('summary'):
        toc_entries.append(('Executive Summary', str(toc_page)))
        toc_page += 1
        body_story.append(_SectionHeader('Executive Summary', brand_color, content_width))
        body_story.append(Spacer(1, 10))
        body_story.append(Paragraph(ai_content['summary'], body_style))
        body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 3 — Key Metrics Overview
    # ────────────────────────────────────────────────────────────
    toc_entries.append(('Key Metrics Overview', str(toc_page)))
    toc_page += 1
    body_story.append(_SectionHeader('Key Metrics Overview', brand_color, content_width))
    body_story.append(Spacer(1, 10))

    for kpi in kpis:
        is_hero = hero is not None and kpi['name'] == hero['name']
        card = _KPICard(
            name=kpi['name'],
            value_str=kpi['value'],
            trend=kpi['trend'],
            trend_pct=kpi.get('trend_pct', 0),
            brand_color_hex=brand_color,
            width=content_width,
            highlight=is_hero,
        )
        body_story.append(card)
        body_story.append(Spacer(1, 8))
    body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 4 — Charts
    # ────────────────────────────────────────────────────────────
    toc_entries.append(('Charts & Visualizations', str(toc_page)))
    toc_page += 1
    body_story.append(_SectionHeader('Charts & Visualizations', brand_color, content_width))
    body_story.append(Spacer(1, 10))

    for i, chart_item in enumerate(chart_paths):
        if isinstance(chart_item, tuple):
            chart_path = chart_item[0]
        else:
            chart_path = chart_item
        if not os.path.isfile(chart_path):
            continue
        try:
            draw_w = content_width
            draw_h = 220
            chart_img = Image(chart_path, width=draw_w, height=draw_h)
            chart_img.hAlign = 'CENTER'
            body_story.append(chart_img)
            body_story.append(Spacer(1, 16))
            if i < len(chart_paths) - 1:
                body_story.append(PageBreak())
        except Exception:
            continue
    body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 5 — AI Insight Cards
    # ────────────────────────────────────────────────────────────
    insights = ai_content.get('insights') or []
    ai_skipped = config.get('_ai_skipped', False)
    if 'insights' in config.get('sections', []):
        toc_entries.append(('AI Insights', str(toc_page)))
        toc_page += 1
        body_story.append(_SectionHeader('AI Insights', brand_color, content_width))
        body_story.append(Spacer(1, 10))
        if ai_skipped:
            body_story.append(Paragraph(
                "⚠️ AI insights were rate-limited during generation. Regenerate this report to include them.",
                body_style,
            ))
        elif insights:
            for ins in insights[:5]:
                card = _InsightCard(ins, width=content_width)
                body_story.append(card)
                body_story.append(Spacer(1, 8))
        body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 6 — Anomaly Flags
    # ────────────────────────────────────────────────────────────
    anomalies = ai_content.get('anomalies') or []
    if anomalies:
        toc_entries.append(('Anomaly Flags', str(toc_page)))
        toc_page += 1
        body_story.append(_SectionHeader('Anomaly Flags', brand_color, content_width))
        body_story.append(Spacer(1, 10))
        for anomaly in anomalies[:10]:
            msg = anomaly.get('message', 'Anomaly detected')
            box = _AnomalyBox(msg, width=content_width)
            body_story.append(box)
            body_story.append(Spacer(1, 6))
        body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 7 — Data Table
    # ────────────────────────────────────────────────────────────
    toc_entries.append(('Data Table', str(toc_page)))
    toc_page += 1
    body_story.append(_SectionHeader('Data Table', brand_color, content_width))
    body_story.append(Spacer(1, 10))

    display_df = df.copy()
    if len(display_df) > 50:
        display_df = display_df.head(50)

    table_data = []
    header_row = [str(c) for c in display_df.columns]
    table_data.append(header_row)
    for _, row in display_df.iterrows():
        table_data.append([str(v) if pd.notna(v) else '' for v in row])

    col_count = len(header_row)
    col_width = min(content_width / max(col_count, 1), 120)
    col_widths = [col_width] * col_count

    data_table = Table(table_data, colWidths=col_widths, repeatRows=1)

    table_style_commands = [
        ('BACKGROUND', (0, 0), (-1, 0), HexColor(brand_color)),
        ('TEXTCOLOR', (0, 0), (-1, 0), white),
        ('FONTNAME', (0, 0), (-1, 0), 'Fraunces-SemiBold'),
        ('FONTSIZE', (0, 0), (-1, 0), 8),
        ('FONTNAME', (0, 1), (-1, -1), 'IBMPlexMono'),
        ('FONTSIZE', (0, 1), (-1, -1), 7),
        ('GRID', (0, 0), (-1, -1), 0.5, HexColor(GRID_LINE)),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('LEFTPADDING', (0, 0), (-1, -1), 4),
        ('RIGHTPADDING', (0, 0), (-1, -1), 4),
        ('TOPPADDING', (0, 0), (-1, -1), 3),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 3),
    ]

    for row_idx in range(1, len(table_data)):
        if row_idx % 2 == 0:
            table_style_commands.append(
                ('BACKGROUND', (0, row_idx), (-1, row_idx), HexColor(ROW_ALT_COLOR))
            )

    data_table.setStyle(TableStyle(table_style_commands))
    body_story.append(data_table)
    body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 8 — Recommendations
    # ────────────────────────────────────────────────────────────
    toc_entries.append(('Recommendations', str(toc_page)))
    toc_page += 1
    body_story.append(_SectionHeader('Recommendations', brand_color, content_width))
    body_story.append(Spacer(1, 10))

    if ai_skipped:
        body_story.append(Paragraph(
            "⚠️ Recommendations were rate-limited during generation. Regenerate this report to include them.",
            body_style,
        ))
    else:
        all_insights = ai_content.get('insights') or []
        actions = [ins.get('action', '') for ins in all_insights if ins.get('action')]

        if actions:
            for idx, action in enumerate(actions, 1):
                body_story.append(_RecommendationCard(idx, action, brand_color, content_width))
                body_story.append(Spacer(1, 8))
        else:
            body_story.append(Paragraph('No recommendations available.', body_style))
    body_story.append(PageBreak())

    # ────────────────────────────────────────────────────────────
    # SECTION 9 — Appendix
    # ────────────────────────────────────────────────────────────
    if 'appendix' in config.get('sections', []):
        toc_entries.append(('Appendix — Raw Data', str(toc_page)))
        toc_page += 1
        body_story.append(_SectionHeader('Appendix — Raw Data', brand_color, content_width))
        body_story.append(Spacer(1, 10))

        appendix_df = df.copy()
        if len(appendix_df) > 100:
            appendix_df = appendix_df.head(100)

        app_table_data = []
        app_header = [str(c) for c in appendix_df.columns]
        app_table_data.append(app_header)
        for _, row in appendix_df.iterrows():
            app_table_data.append([str(v) if pd.notna(v) else '' for v in row])

        app_col_count = len(app_header)
        app_col_width = min(content_width / max(app_col_count, 1), 120)
        app_col_widths = [app_col_width] * app_col_count

        app_table = Table(app_table_data, colWidths=app_col_widths, repeatRows=1)

        app_style_commands = [
            ('BACKGROUND', (0, 0), (-1, 0), HexColor(brand_color)),
            ('TEXTCOLOR', (0, 0), (-1, 0), white),
            ('FONTNAME', (0, 0), (-1, 0), 'Fraunces-SemiBold'),
            ('FONTSIZE', (0, 0), (-1, 0), 8),
            ('FONTNAME', (0, 1), (-1, -1), 'IBMPlexMono'),
            ('FONTSIZE', (0, 1), (-1, -1), 7),
            ('GRID', (0, 0), (-1, -1), 0.5, HexColor(GRID_LINE)),
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
            ('LEFTPADDING', (0, 0), (-1, -1), 4),
            ('RIGHTPADDING', (0, 0), (-1, -1), 4),
            ('TOPPADDING', (0, 0), (-1, -1), 3),
            ('BOTTOMPADDING', (0, 0), (-1, -1), 3),
        ]
        for row_idx in range(1, len(app_table_data)):
            if row_idx % 2 == 0:
                app_style_commands.append(
                    ('BACKGROUND', (0, row_idx), (-1, row_idx), HexColor(ROW_ALT_COLOR))
                )

        app_table.setStyle(TableStyle(app_style_commands))
        body_story.append(app_table)

    # ────────────────────────────────────────────────────────────
    # TOC — built from recorded entries, inserted after cover
    # ────────────────────────────────────────────────────────────
    story.append(_SectionHeader('Table of Contents', brand_color, content_width))
    story.append(Spacer(1, 12))
    for label, pg in toc_entries:
        dots = '.' * max(2, 60 - len(label) - len(pg))
        line = f'{label}  {dots}  {pg}'
        story.append(Paragraph(line, toc_h1_style))
    story.append(PageBreak())

    story.extend(body_story)

    doc.build(story, onFirstPage=on_page, onLaterPages=on_page)
    logging.info(f"[pdf_service] build_sync completed — report_id={report_id}")

    _strip_standard_fonts(pdf_path)

    if logo_path:
        try:
            os.unlink(logo_path)
        except OSError:
            pass

    return pdf_path
