        bg = _brand_tint(self.brand_color_hex) if self.highlight else HexColor('#F9FAFB')
        self.canv.setFillColor(bg)
        self.canv.roundRect(0, 0, self._width, card_h, 6, fill=1, stroke=0)
        self.canv.setFillColor(HexColor('#374151'))
